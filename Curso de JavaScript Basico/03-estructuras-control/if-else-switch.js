// SWITCH

let variable = 4;

switch (variable) {
    case 1:
        console.log("La variable es 1");
        break;
    case 2:
        console.log("La variable es 2");
        break;
    case 3:
        console.log("La variable es 3");
        break;
    case 4:
        console.log("La variable es 4");
        break;
    case 5:
        console.log("La variable es 5");
        break;
    default:
        console.log("No se cumple ningun caso");
        break;
}